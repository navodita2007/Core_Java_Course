package com.navodita.control_statement.nested_for_loop_programs.star_pattern_programs;

public class StarPattern2 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
